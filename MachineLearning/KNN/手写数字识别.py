from numpy import *
import os
import operator

def img2vector (filename):
    returnVect = zeros((1,1024))
    fr = open(filename,'r', encoding='UTF-8')
    for i in range(32):
        lineStr = fr.readline()
        for j in range(32):
            returnVect[0, 32*i+j] = int(lineStr[j])
    return returnVect

